public class floatingpointnumberchallenge {

    public static void main(String[] args) {

        int myIntegerValue = 5;

        double myDoubleValue = 0.45359237;

        double myTotalValue = myIntegerValue * myDoubleValue;

        System.out.println("The total result is: " + myTotalValue + " kilograms");

        double pi = 3.1415927d;
        double anotherNumber = 3_000_000.4_567_890d;
        System.out.println(pi);
        System.out.println(anotherNumber);
    }
}
